print("\n\nNúmero de 1 a 5 multiplicado por 2 com for:")
for numero in range(1, 6):
    print(numero * 2)

print("\n\nNúmero de 1 a 5 multiplicado por 2 com While:")
contador = 1
while contador <= 5:
    print(contador * 2)
    contador += 1